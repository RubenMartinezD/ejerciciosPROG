/**
    * Función para preguntar al usuario un número
    * @type {string} numerousr - Número introducido
    * @returns {number} numerousr - Cadena de texto pasada a número
*/
function dimenumero() {
    var numerousr = prompt("Introduce un numerote entre 1 y 100 porfa")
    numerousr = parseInt(numerousr)
    return numerousr
}
